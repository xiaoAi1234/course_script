def read_file_content(file_path):
    """
    通过文件路径读取文件内容
    """
    # 检查文件是否存在
    if not os.path.exists(file_path):
        print(f"错误：找不到文件 '{file_path}'，请检查路径是否正确")
        return None

    try:
        # 读取文件内容
        with open(file_path, 'r', encoding='utf-8') as file:
            content = file.read()
            return content
    except Exception as e:
        print(f"读取文件时发生错误：{e}")
        return None

def process_document(file_content):
    # 1. 解析文本，转换为字典数组
    # 使用正则表达式按照 "第 X 页" 切分文本
    raw_pages = re.split(r'第\s*\d+\s*页\s*\n', file_content.strip())

    dict_array = []

    # 将每页ppt内容转换为字典对象
    for page in raw_pages:
        page = page.strip()
        if not page:
            continue

        # 按行切分，第一行是 type，剩下的是 content
        lines = page.split('\n', 1)
        if len(lines) >= 1:
            page_type = lines[0].strip()
            # 如果有内容则保留，否则为空字符串
            content = lines[1].strip() if len(lines) > 1 else ""

            dict_array.append({
                "type": page_type,
                "content": content
            })

    return dict_array

